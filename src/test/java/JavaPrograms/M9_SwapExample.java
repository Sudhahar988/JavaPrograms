package JavaPrograms;

public class M9_SwapExample {

	
	public static void main(String[] args)
	{
		
		System.out.println("JAVA SWAP PROGRAM");
		int x=10;
		int y=20;
		
		x=x+y;
		y=x-y;
		x=x-y;
		
		System.out.println("X value is:"+x);
		System.out.println("Y value is:"+y);
		
	}
}